    private void HienThi() {
        DefaultTableModel tableModel = (DefaultTableModel) tableNhanVIen.getModel();
        tableModel.setColumnCount(0);
        tableModel.setRowCount(0);
        tableModel.addColumn("Mã nhân viên");
        tableModel.addColumn("Tên nhân viên");
        tableModel.addColumn("Chức vụ");
        tableModel.addColumn("Lương");
        ArrayList<NhanVien> data = qlnv.getData();
        for (NhanVien nhanVien : data) {
            tableModel.addRow(new Object[]{nhanVien.getMaNV(), nhanVien.getTenNV(), nhanVien.getChucVu(), nhanVien.getLuong()});
        }
    }

