    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String maNV = txtmaNV.getText();
        String tenNV = txttenNV.getText();
        String chucVu = txtchucVu.getText();
        int luong = Integer.parseInt(txtLuong.getText());
        qlnv.ThemNhanVien(new NhanVien(maNV, tenNV, chucVu, luong));
        JOptionPane.showMessageDialog(this, "Thêm thành công !");
        HienThi();
    }//GEN-LAST:event_btnThemActionPerformed

    private void tableNhanVIenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableNhanVIenMouseClicked
        // TODO add your handling code here:
        int row = tableNhanVIen.getSelectedRow();
        NhanVien nV= qlnv.getData().get(row);
        txtmaNV.setText(nV.getMaNV());
        txttenNV.setText(nV.getTenNV());
        txtchucVu.setText(nV.getChucVu());
        txtLuong.setText(String.valueOf(nV.getLuong()));
    }//GEN-LAST:event_tableNhanVIenMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        String maNV = txtmaNV.getText();
        boolean kt = qlnv.Xoa(maNV);
        if (kt) {
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            HienThi();

        } else {
            JOptionPane.showMessageDialog(this, "Không xóa được");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
       String maNV = txtmaNV.getText();
        String tenNV = txttenNV.getText();
        String chucVu = txtchucVu.getText();
        int luong = Integer.parseInt(txtLuong.getText());
        NhanVien nh = new NhanVien(maNV, tenNV, chucVu, luong);
        boolean kt = qlnv.Sua(nh);
        if (kt) {
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            HienThi();
        } else {
            JOptionPane.showMessageDialog(this, "Không sửa được");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

